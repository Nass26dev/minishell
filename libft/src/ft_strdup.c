#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*dest;
	int		len;

	i = 0;
	len = ft_strlen(s);
	if (len < 0)
		return (NULL);
	dest = malloc(sizeof(char) * ft_strlen(s) + 1);
	if (!dest)
		return (NULL);
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
